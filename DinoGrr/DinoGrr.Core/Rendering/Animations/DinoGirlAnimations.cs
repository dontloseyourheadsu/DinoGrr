namespace DinoGrr.Core.Rendering.Animations;

/// <summary>
/// Represents the different animations available for the DinoGirl character.
/// </summary>
public enum DinoGirlAnimation
{
    /// <summary>
    /// The idle animation when the DinoGirl is not moving.
    /// </summary>
    Running,
    /// <summary>
    /// The animation when the DinoGirl is jumping.
    /// </summary>
    Hit,
    /// <summary>
    /// The animation when the DinoGirl is hit by an enemy or obstacle.
    /// </summary>
    Idle
}
